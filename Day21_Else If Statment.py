# ELIF = Else If Statment

nama = input("Siapa nama Anda? ")

if nama == "Sinar": # kondisi 1
    print("Hai, sudah makan hari ini?") # aksi true 1
elif nama == "Samsinar": # kondisi 2
    print("Hai, sudah belajar hari ini?") # aksi true 2
elif nama == "Narr": # kondisi 3
    print("Hai, mau jalan kemana lagi?") # aksi true 3
else:
    print("Anda siapa??") # aksi false

print("Ini akhir dari program")