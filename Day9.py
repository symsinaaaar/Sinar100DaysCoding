##Operasi Komparasi
# Setiap hasil dari operasi komperasi adalah boolean

# >, <, >=, <=, ==, !=, is, is not

a = 5
b = 3

# lebih besar dari >
print('========== lebih besar dari (>)')
hasil = a > 4
print(a,'>',4,'=', hasil)
hasil = b > 4
print(b,'>',4,'=', hasil)

# kurang dari <
print('========== kurang dari (<)')
hasil = a < 4
print(a,'<',4,'=', hasil)
hasil = b < 4
print(b,'<',4,'=', hasil)

# lebih besar dari sama dengan >=
print('========== lebih besar dari sama dengan (>=)')
hasil = a >= 4
print(a,'>=',4,'=', hasil)
hasil = b >= 4
print(b,'>=',4,'=', hasil)

# kurang dari sama dengan >
print('========== kurang dari sama dengan (<=)')
hasil = a <= 4
print(a,'<=',4,'=', hasil)
hasil = b <= 4
print(b,'<=',4,'=', hasil)

# sama dengan ==
print('========== sama dengan (==)')
hasil = a == 4
print(a,'==',4,'=', hasil)
hasil = b == 4
print(b,'==',4,'=', hasil)

# tidak sama dengan !=
print('========== sama dengan (!=)')
hasil = a != 4
print(a,'!=',4,'=', hasil)
hasil = b != 4
print(b,'!=',4,'=', hasil)



