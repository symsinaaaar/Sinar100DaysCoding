##Operasi Aritmatika
a = 5
b = 15
# Operasi Tambah (+)
hasil = a + b
print(a,'+',b,'=', hasil)

#Operasi Pengurangan (-)
hasil = a - b
print(a,'-',b,'=', hasil)

#Operasi Perkalian (*)
hasil = a * b
print(a,'*',b,'=', hasil)

#Operasi Pembagian (/)
hasil = a / b
print(a,'/',b,'=', hasil)

#Operasi Eksponen (Pangkat) **
hasil = a ** b
print(a,'**',b,'=', hasil)

#Operasi Modulus (Sisa Pembagian) %
hasil = a % b
print(a,'%',b,'=', hasil)

#Operasi Floor Division (Kebalikan Modulus) //
hasil = a // b
print(a,'//',b,'=', hasil)