# ELIF = Else If Statment

nama = input("Siapa nama Anda? ")

if nama == "Sinar":
    print("Hai, sudah makan hari ini?")
elif nama == "Samsinar":
    print("Hai, sudah belajar hari ini?")
elif nama == "Narr":
    print("Hai, mau jalan kemana lagi?")
else:
    print("Anda siapa??")

print("Ini akhir dari program")